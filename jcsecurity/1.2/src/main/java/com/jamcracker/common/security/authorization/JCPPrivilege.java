package com.jamcracker.common.security.authorization;

import java.io.Serializable;
import java.util.Date;

/**
 * JCP user privileges.
 */
public class JCPPrivilege implements Serializable {

	private static final long serialVersionUID = 1L;

	private Integer privilegeId;

	private int parentPrivId;

	private String name;

	private String description;

	private String code;

	private String serviceId;

	private int type;

	private Date startDate;

	private Date endDate;

	private Date creationDate;

	private int createdBy;

	private Date updateDate;

	private int updatedBy;

	public Integer getPrivilegeId() {
		return privilegeId;
	}

	public void setPrivilegeId(Integer privilegeId) {
		this.privilegeId = privilegeId;
	}

	public int getParentPrivId() {
		return parentPrivId;
	}

	public void setParentPrivId(int parentPrivId) {
		this.parentPrivId = parentPrivId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getServiceId() {
		return serviceId;
	}

	public void setServiceId(String serviceId) {
		this.serviceId = serviceId;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public Date getStartDate() {
		return startDate;
	}

	public void setStartDate(Date startDate) {
		this.startDate = startDate;
	}

	public Date getEndDate() {
		return endDate;
	}

	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}

	public Date getCreationDate() {
		return creationDate;
	}

	public void setCreationDate(Date creationDate) {
		this.creationDate = creationDate;
	}

	public int getCreatedBy() {
		return createdBy;
	}

	public void setCreatedBy(int createdBy) {
		this.createdBy = createdBy;
	}

	public Date getUpdateDate() {
		return updateDate;
	}

	public void setUpdateDate(Date updateDate) {
		this.updateDate = updateDate;
	}

	public int getUpdatedBy() {
		return updatedBy;
	}

	public void setUpdatedBy(int updatedBy) {
		this.updatedBy = updatedBy;
	}

	public String toString() {
		return this.name;
	}
}
